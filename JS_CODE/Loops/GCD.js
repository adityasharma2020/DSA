// Function definition 1 (2 arguments)
function myFunction(a, b) {
    console.log("Two arguments:", a, b);
}

// Function definition 2 (3 arguments)
function myFunction(a, b, c) {
    console.log("Three arguments:", a, b, c);
}

// Call the function with two arguments
myFunction(1, 2); // Outputs: Three arguments: 1 2 undefined

// Call the function with three arguments
myFunction(1, 2, 3); // Outputs: Three arguments: 1 2 3
